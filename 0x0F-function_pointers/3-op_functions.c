#include "3-calc.h"

/**
 * op_add - function that return the sum of two integers
 * @a: num 1
 * @b: num 2
 * Return: a+b
 */
int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
 * op_sub - that return the difference between two integers
 * @a: num 1
 * @b: num 2
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - that return the multiplication of two integers
 * @a: num 1
 * @b: num 2
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - that return the division of two integers
 * @a: num 1
 * @b: num 2
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod _ that return the module of two integers
 * @a: num 1
 * @b: num 2
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
